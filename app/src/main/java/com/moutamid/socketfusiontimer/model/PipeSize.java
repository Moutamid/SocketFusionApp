package com.moutamid.socketfusiontimer.model;

public class PipeSize {
    private String name;
    private String color;

    public PipeSize(String name, String color) {
        this.name = name;
        this.color = color;
    }

    public String getName() {
        return name;
    }

    public String getColor() {
        return color;
    }
}
